export const normalizeLanguage = (value) => {
  const raw = String(value || "")
    .trim()
    .toLowerCase();
  if (raw in SUPPORTED_LANGUAGES) return raw;
  // allow values like en-US
  const short = raw.split(/[-_]/)[0];
  if (short in SUPPORTED_LANGUAGES) return short;
  return DEFAULT_LANGUAGE;
};

